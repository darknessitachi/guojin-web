package com.xun360.guojin.client.bean;

public class FundsResponse {

	private double initFunds;
	private double profit;
	private double dynamicRight;
	private double staticRight;
	private double deposit;
	private double expendableFunds;
	private double freeze;
	private double risk;
	public double getInitFunds() {
		return initFunds;
	}
	public void setInitFunds(double initFunds) {
		this.initFunds = initFunds;
	}
	public double getProfit() {
		return profit;
	}
	public void setProfit(double profit) {
		this.profit = profit;
	}
	public double getDynamicRight() {
		return dynamicRight;
	}
	public void setDynamicRight(double dynamicRight) {
		this.dynamicRight = dynamicRight;
	}
	public double getStaticRight() {
		return staticRight;
	}
	public void setStaticRight(double staticRight) {
		this.staticRight = staticRight;
	}
	public double getDeposit() {
		return deposit;
	}
	public void setDeposit(double deposit) {
		this.deposit = deposit;
	}
	public double getExpendableFunds() {
		return expendableFunds;
	}
	public void setExpendableFunds(double expendableFunds) {
		this.expendableFunds = expendableFunds;
	}
	public double getFreeze() {
		return freeze;
	}
	public void setFreeze(double freeze) {
		this.freeze = freeze;
	}
	public double getRisk() {
		return risk;
	}
	public void setRisk(double risk) {
		this.risk = risk;
	}
	
	
}
